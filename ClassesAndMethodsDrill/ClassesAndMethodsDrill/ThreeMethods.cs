using System;
using System.Collections.Generic;
using System.Text;


namespace ClassesAndMethodsDrill
{
    class ThreeMethods
    {
        public static int Add75(int num)
        {
            num = num + 75;

            return num;
        }

        public static int Divide50(int num)
        {
            num = num / 50;

            return num;
        }

        public static int Times25(int num)
        {
            num = num * 25;

            return num;
        }
    }
}
